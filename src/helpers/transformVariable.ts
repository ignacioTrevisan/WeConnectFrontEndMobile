export const TransformVariable = (nombre: string) => {

    switch (nombre) {
        case 'Nombre: ':
            return 'Name'
            break;
        case 'Apellido: ':
            return 'LastName'
            break;
        case 'Genero: ':
            return 'genero'
            break;
        case 'Fecha nacimiento: ':
            return 'fechaNacimiento'
            break;
        case 'DisplayName: ':
            return 'DisplayName'
            break;
        case 'Email: ':
            return 'Email'
            break;
        default:
            'numeroDeTelefono'
    }



    // numero de telefono
}